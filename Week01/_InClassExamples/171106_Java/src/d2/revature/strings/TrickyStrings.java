package d2.revature.strings;

public class TrickyStrings {

	public static void main(String[] args) {
		System.out.println(1 + 2 + 3 + 4); //
		System.out.println(1 + 2 + 3 + "4");
		System.out.println(1 + 2 + "3" + "4");
		System.out.println(1 + "2" + 3 + "4");
		System.out.println(1 + "2" + 3 + 4);
	}

}
